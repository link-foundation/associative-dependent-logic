#!/usr/bin/env node

import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import { createRequire } from 'node:module';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..');
const jsRequire = createRequire(path.join(repoRoot, 'js', 'package.json'));
const esbuild = jsRequire('esbuild');

const sourceFile = path.join(repoRoot, 'js', 'src', 'rml-links.mjs');
const outputFile = path.join(repoRoot, 'docs', 'playground', 'rml-playground-runtime.mjs');

const browserShims = `const process = {
  argv: [],
  env: {},
  cwd: () => '/',
  exit(code = 0) {
    throw new Error(\`The RML CLI is not available in the browser playground (exit \${code}).\`);
  },
};

function unavailableNodeApi(name) {
  return () => {
    throw new Error(\`\${name} is not available in the browser playground. Paste dependencies inline instead.\`);
  };
}

const fs = {
  readFileSync: unavailableNodeApi('File system reads'),
  writeFileSync: unavailableNodeApi('File system writes'),
};

const path = {
  basename(input) {
    const value = String(input || '').replace(/\\\\/g, '/').replace(/\\/+$/g, '');
    const index = value.lastIndexOf('/');
    return index === -1 ? value : value.slice(index + 1);
  },
  dirname(input) {
    const value = String(input || '').replace(/\\\\/g, '/').replace(/\\/+$/g, '');
    const index = value.lastIndexOf('/');
    if (index <= 0) return '/';
    return value.slice(0, index);
  },
  isAbsolute(input) {
    return String(input || '').startsWith('/');
  },
  resolve(...parts) {
    const joined = parts.filter(Boolean).map(String).join('/');
    const absolute = joined.startsWith('/') ? joined : \`/\${joined}\`;
    const stack = [];
    for (const part of absolute.split('/')) {
      if (!part || part === '.') continue;
      if (part === '..') stack.pop();
      else stack.push(part);
    }
    return \`/\${stack.join('/')}\`;
  },
};

function spawnSync() {
  return {
    status: 127,
    stdout: '',
    stderr: 'External solver processes are not available in the browser playground.',
    error: new Error('External solver processes are not available in the browser playground.'),
  };
}
`;

function browserEntrySource() {
  let source = fs.readFileSync(sourceFile, 'utf8');
  source = source.replace(/^#!.*\n/, '');
  source = source.replace(
    [
      "import fs from 'node:fs';",
      "import path from 'node:path';",
      "import { spawnSync } from 'node:child_process';",
    ].join('\n'),
    browserShims,
  );
  source = source.replace(
    "if (import.meta.url === `file://${process.argv[1]}`) {",
    'if (false) {',
  );
  return source;
}

const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), 'rml-playground-build-'));
const tempEntry = path.join(tempDir, 'rml-links-browser-entry.mjs');

try {
  fs.writeFileSync(tempEntry, browserEntrySource(), 'utf8');
  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  await esbuild.build({
    absWorkingDir: path.join(repoRoot, 'js'),
    nodePaths: [path.join(repoRoot, 'js', 'node_modules')],
    entryPoints: [tempEntry],
    outfile: outputFile,
    bundle: true,
    format: 'esm',
    platform: 'browser',
    target: 'es2020',
    treeShaking: true,
    legalComments: 'inline',
    banner: {
      js: '// Generated by scripts/build-playground.mjs; do not edit by hand.',
    },
  });
  console.log(`Wrote ${path.relative(repoRoot, outputFile)}`);
} finally {
  fs.rmSync(tempDir, { recursive: true, force: true });
}
